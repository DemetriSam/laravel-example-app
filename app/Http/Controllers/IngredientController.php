     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Ingredient $ingredient)
    {
        $ingredient->name = $request->name;
        $ingredient->fats = $request->fats;
        $ingredient->carbs = $request->carbs;
        $ingredient->proteins = $request->proteins;
        $ingredient->fiber = $request->fiber;
        $ingredient->ccal = $request->ccal;
        $ingredient->description = $request->description;
        $ingredient->type = $request->type;
    
        $ingredient->save();
        return view('create-ingredient-form');
    }

